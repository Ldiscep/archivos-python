#se usan para comparar valores numericos
#permiten obtener un valor bool, pueden ser los siguientes:
"""
< (menor que)
> (mayor que)
<= (menor o igual que)
>= (mayor o igual que)
== (igual)
!= (diferente)
"""
numero_uno=10
numero_dos=50
numero_tres=10

resultado= numero_uno < numero_dos
print(resultado)
#tambien puede hacerse con numeros, en vez de variables
segundo_resultado= 10<4
print(segundo_resultado)

tercer_resultado= numero_tres == numero_uno
print(tercer_resultado)

cuarto_resultado= numero_uno != numero_dos
print(cuarto_resultado)
