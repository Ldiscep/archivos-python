#int
"""
Permite trabajar con numeros enteros (- o +), no hay que usar "" para escribir datos de tipo int o float
se pueden hacer calculos matem√°ticos entre numeros (+, -, *, /) para dividir numeros, y que el resultado de un numero exacto se usan dos barras (//)
"""
primer_numero=10
print(primer_numero)

segundo_numero=-10
print(segundo_numero)

multiplicacion=(10*20) #ademas se pueden usar + - /
