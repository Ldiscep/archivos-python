#si queremos imprimir una sola vez un elemento o varios, sin crear una variable, se pude hacer con la función print
nombre="Luciano"
apellido="Discepolo"

print(nombre, "Alfonso", apellido,) #la función print por defecto separa los valores con un espacio
#para separar los valores con otro caracter, hay que usar la función "sep", ejemplo abajo
print(nombre, "Alfonso", apellido, sep= "-")